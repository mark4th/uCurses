#include <stdlib.h>

static void draw_lion(void)
{
    int x, y, i;
    int16_t chr;
    uint16_t code;
    uint16_t *braile_data;

    braile_data = calloc(win->width * win->height, 2);

    uC_braille_8(win, braile_data, lion_data, LION_WIDTH);

    // you can do further drawing into the braile data here maybe

    uC_draw_braille(win, braile_data);

    free(braile_data);
}

// -----------------------------------------------------------------------

void lion(void)
{
    while(uC_test_keys() != 0)
    {
        uC_key();
    }

    draw_lion();
    uC_scr_draw_screen(scr);

    do
    {
        ;
    } while((uC_test_keys() == 0) && (uC_key() != 0x1b));
}

// -----------------------------------------------------------------------

static void exit_prog(void)
{
    uC_set_key(0x1b);
}

// -----------------------------------------------------------------------

static uC_switch_t menu_vectors[] =
{
    { 0x8d9c616c, exit_prog }
};

#define VCOUNT sizeof(menu_vectors) / sizeof(menu_vectors[0])

// -----------------------------------------------------------------------

opt_t menu_address_cb(int32_t hash)
{
    int16_t i;
    uC_switch_t *s = menu_vectors;

    for(i = 0; i < VCOUNT; i++)
    {
        if(hash == s->option)
        {
            return s->vector;
        }
        s++;
    }

    return NULL;
}

// -----------------------------------------------------------------------

int main(void)
{
    uC_list_node_t *n;

    uCurses_init();
    uC_json_file_create_ui("json/dots.json", menu_address_cb);
    uC_menu_init();

    scr = active_screen;
    n   = scr->windows.head;
    win = n->payload;

    status_win = uC_add_status(scr, 32, 55, 0);

    uC_win_printf(status_win, "%fs%bs%0", 9, 3);

    uC_set_status(status_win, status);
    uC_clr_status(status_win);

    lion();

    uC_scr_close(active_screen);

    uC_console_reset_attrs();
    uC_clear();
    uC_cup(10, 0);

    uCurses_deInit();

    printf("Au revoir!\n");

    return 0;
}


